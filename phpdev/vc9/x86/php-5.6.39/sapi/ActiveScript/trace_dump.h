char *format_zval(zval *z, char *buffer)
{
	/* static char buffer[BUFFER_LEN]; */
	 
	switch(z->type) {
		case IS_NULL:
			return "NULL";
		case IS_LONG:
		case IS_BOOL:
			snprintf(buffer, BUFFER_LEN, "%d", z->value.lval);
			return buffer;
		case IS_DOUBLE:
			snprintf(buffer, BUFFER_LEN, "%f", z->value.dval);
			return buffer;
		case IS_STRING:
			snprintf(buffer, BUFFER_LEN, "\"%s\"", z->value.str.val);
			return buffer;
		case IS_RESOURCE:
		case IS_ARRAY:
		case IS_OBJECT:		
		case IS_CONSTANT:
		/* case IS_CONSTANT_ARRAY: /* Removed, see phpdbg_info.c */
			return "";
		default:
			return "unknown";
	}
}
